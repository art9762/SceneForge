"""Producer agent — creates production notes."""

from __future__ import annotations

from typing import ClassVar

from pydantic import BaseModel

from .base import BaseAgent
from ..schemas.production import ProductionInput, ProductionNotes


class ProducerAgent(BaseAgent):
    name: ClassVar[str] = "producer"
    description: ClassVar[str] = "Создаёт продакшн-документацию: шот-лист, локации, реквизит"
    system_prompt: ClassVar[str] = """Ты — линейный продюсер видеопродакшна. Твоя задача — превратить утверждённый сценарий в производственную документацию.

Правила работы:
1. Составь подробный шот-лист: для каждой сцены укажи тип кадра (общий, средний, крупный, деталь, дрон и т.д.).
2. Определи все необходимые локации. Укажи, можно ли заменить студией или хромакеем.
3. Составь список реквизита для каждой сцены, включая мелочи (кружка, книга, ноутбук).
4. Определи потребности в архивных кадрах: что именно нужно найти, где искать (стоки, архивы, newsreel).
5. Опиши графику и анимацию: какие инфографики, титры, lower thirds, анимированные вставки нужны.
6. Выяви производственные сложности: сложный свет, много локаций за день, погодозависимость, разрешения на съёмку.
7. Для каждого шота укажи заметки: нужен ли стедикам, штатив, слайдер, особый объектив.
8. Продумай порядок съёмки для оптимизации времени (все сцены на одной локации подряд).
9. Отметь, что можно снять самостоятельно, а что требует команды.
10. Укажи, какие сцены можно заменить скринкастом, анимацией или стоковым видео.
11. Думай об экономии бюджета без потери качества.
12. Если есть люди в кадре — укажи необходимость актёров, статистов, экспертов."""
    input_schema: ClassVar[type[BaseModel]] = ProductionInput
    output_schema: ClassVar[type[BaseModel]] = ProductionNotes
    default_provider: ClassVar[str] = "claude"
    default_model: ClassVar[str] = ""
